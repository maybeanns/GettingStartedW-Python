#collections are : Lists, Tuples, Sets, Dictionaries

# Lists are mutable, ordered collections of items that can be of any data type, including strings, integers, floats, and other lists.
# Tuples are immutable, ordered collections of items that can be of any data type, including strings, integers, floats, and other tuples.
# Sets are unordered collections of unique items that can be of any data type, including strings, integers, floats, and other sets.
# Dictionaries are unordered collections of key-value pairs, where each key is unique and maps to a value that can be of any data type, including strings, integers, floats, and other dictionaries.
# Immutable collections are collections that cannot be changed after they are created. This means that you cannot add, remove, or modify items in an immutable collection. Examples of immutable collections in Python include tuples and frozensets.
# Mutable collections are collections that can be changed after they are created. This means that you can add, remove, or modify items in a mutable collection. Examples of mutable collections in Python include lists, sets, and dictionaries.

